__________RIAS GREMORY__________()
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	string s;
	cin >> s;
	vector<pair<string, int>> v;
	for (int i = 0; i < s.size(); i++)	{
		string ntk = "";
		if (isalpha(s[i])){
			ntk += s[i];
			i++;
			if (i < s.size() && islower(s[i])){
				ntk += s[i];
				i++;
			}
		}
		string num = "";
		while (i < s.size() && isdigit(s[i]))
		{
			num += s[i];
			i++;
		}
		i--;
		if (num == "")	{
			num = "1";
		}
		int number = stoi(num);
		v.push_back({ ntk,number });
	}
	for (auto x : v) {
		cout << x.first << " " << x.second << endl;
	}
}


